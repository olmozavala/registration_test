def load_image(path: Path) -> np.ndarray:
    """Load an image from disk as an RGB uint8 array.

    Parameters:
        path: Image path to load.

    Returns:
        Image data with shape (height, width, 3).
    """
    with Image.open(path) as image:
        return np.asarray(image.convert("RGB"), dtype=np.uint8)


def load_nifti_volume(path: Path) -> np.ndarray:
    """Load a 3D NIfTI file as a floating point volume.

    Parameters:
        path: Path to a NIfTI image.

    Returns:
        NIfTI voxel data with shape (height, width, slices). For 4D data,
        the first trailing volume/channel is used.
    """
    nifti_image = nib.load(str(path))
    volume = np.asarray(nifti_image.get_fdata(dtype=np.float32))
    volume = np.squeeze(volume)

    while volume.ndim > 3:
        volume = volume[..., 0]
    if volume.ndim == 2:
        volume = volume[:, :, np.newaxis]
    if volume.ndim != 3:
        raise ValueError(f"Expected a 3D NIfTI volume, got shape {volume.shape}.")

    return volume


def cache_image(image: np.ndarray) -> str:
    """Store image data in the server cache and return its lookup key.

    Parameters:
        image: Image or volume array to cache.

    Returns:
        Unique key for retrieving the cached image data.
    """
    key = str(uuid.uuid4())
    IMAGE_CACHE[key] = image
    return key


def cached_image(key: str) -> np.ndarray:
    """Retrieve image data from the server cache.

    Parameters:
        key: Cache key generated by cache_image.

    Returns:
        Cached image or volume array.
    """
    return IMAGE_CACHE[key]


def rotate_clockwise(image: np.ndarray) -> np.ndarray:
    """Rotate an image or volume 90 degrees clockwise in the display plane.

    Parameters:
        image: Image array with at least two spatial dimensions.

    Returns:
        Rotated image array.
    """
    return np.rot90(image, k=-1, axes=(0, 1)).copy()


def flip_horizontal(image: np.ndarray) -> np.ndarray:
    """Flip an image or volume horizontally in the display plane.

    Parameters:
        image: Image array with at least two spatial dimensions.

    Returns:
        Horizontally flipped image array.
    """
    return np.flip(image, axis=1).copy()


def array_to_rgb_image(array: np.ndarray) -> np.ndarray:
    """Convert a NumPy array into a displayable RGB image.

    Parameters:
        array: Input image array from a NumPy file.

    Returns:
        RGB uint8 image with shape (height, width, 3).
    """
    image = np.asarray(array)
    image = np.squeeze(image)

    if image.ndim == 2:
        return normalize_slice_to_rgb(image.astype(float))

    if image.ndim == 3 and image.shape[-1] in {3, 4}:
        rgb_image = image[:, :, :3]
        if rgb_image.dtype == np.uint8:
            return rgb_image

        finite_values = rgb_image[np.isfinite(rgb_image)]
        if finite_values.size == 0:
            return np.zeros((*rgb_image.shape[:2], 3), dtype=np.uint8)

        minimum = float(np.min(finite_values))
        maximum = float(np.max(finite_values))
        if maximum <= 1.0 and minimum >= 0.0:
            scaled = rgb_image * 255.0
        elif np.isclose(minimum, maximum):
            scaled = np.zeros_like(rgb_image, dtype=float)
        else:
            scaled = 255.0 * (rgb_image - minimum) / (maximum - minimum)
        return np.clip(scaled, 0, 255).astype(np.uint8)

    if image.ndim == 3:
        middle_slice = image[:, :, image.shape[2] // 2]
        return normalize_slice_to_rgb(middle_slice.astype(float))

    raise ValueError(f"Cannot display NumPy array with shape {image.shape}.")


def load_npy_image(path: Path) -> np.ndarray:
    """Load a NumPy image file as RGB data.

    Parameters:
        path: Path to a NumPy .npy file.

    Returns:
        RGB uint8 image.
    """
    return array_to_rgb_image(np.load(path))


def npy_data_uri_to_image(data_uri: str) -> np.ndarray:
    """Decode an uploaded NumPy .npy data URI into RGB image data.

    Parameters:
        data_uri: Uploaded NumPy file data URI.

    Returns:
        RGB uint8 image.
    """
    _, encoded = data_uri.split(",", maxsplit=1)
    raw = base64.b64decode(encoded)
    with io.BytesIO(raw) as buffer:
        array = np.load(buffer)
    return array_to_rgb_image(array)


def default_moving_image_data_uri() -> str:
    """Load the preferred default moving image source.

    Parameters:
        None.

    Returns:
        Base64 PNG data URI for the moving image display.
    """
    if DEFAULT_MOVING_NPY.exists():
        return image_to_data_uri(load_npy_image(DEFAULT_MOVING_NPY))

    return default_image_data_uri(DEFAULT_MOVING_IMAGE)


def uploaded_moving_image_data_uri(contents: str, filename: str | None) -> str:
    """Decode an uploaded moving image from NPY or standard image formats.

    Parameters:
        contents: Uploaded file data URI.
        filename: Uploaded filename used to infer file type.

    Returns:
        Base64 PNG data URI for the moving image display.
    """
    if filename and filename.endswith(".npy"):
        return image_to_data_uri(npy_data_uri_to_image(contents))

    return contents


def rotated_fixed_source_info(
    source_info: SourceInfo,
    slice_index: int | None,
) -> SourceInfo:
    """Rotate the cached fixed source and return updated metadata.

    Parameters:
        source_info: Metadata for the active fixed image or volume.
        slice_index: Current fixed slice index to preserve.

    Returns:
        Metadata for the rotated fixed image or volume.
    """
    rotated_image = rotate_clockwise(cached_image(str(source_info["key"])))
    updated_info = make_fixed_source_info(
        rotated_image,
        str(source_info["name"]),
        str(source_info["kind"]),
    )
    updated_info["slice_index"] = int(slice_index or source_info["slice_index"])
    return updated_info


