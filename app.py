def register_callbacks(app: Dash) -> None:
    """Register Dash callbacks for image loading, landmarks, and registration.

    Parameters:
        app: Dash application instance.

    Returns:
        None.
    """

    @app.callback(
        Output("fixed-source-store", "data"),
        Input("fixed-upload", "contents"),
        Input("rotate-fixed", "n_clicks"),
        Input("flip-fixed", "n_clicks"),
        State("fixed-upload", "filename"),
        State("fixed-source-store", "data"),
        State("fixed-slice-slider", "value"),
        prevent_initial_call=True,
    )
    def update_fixed_image(
        contents: str | None,
        rotate_clicks: int | None,
        flip_clicks: int | None,
        filename: str | None,
        source_info: SourceInfo,
        slice_index: int | None,
    ) -> SourceInfo:
        """Update the fixed image store after an upload.

        Parameters:
            contents: Uploaded image data URI.
            rotate_clicks: Number of fixed-image rotation button clicks.
            flip_clicks: Number of fixed-image horizontal flip button clicks.
            filename: Uploaded filename.
            source_info: Metadata for the active fixed source.
            slice_index: Current fixed slice index.

        Returns:
            Metadata for the uploaded fixed image source.
        """
        if ctx.triggered_id == "rotate-fixed":
            if not rotate_clicks:
                raise PreventUpdate
            return rotated_fixed_source_info(source_info, slice_index)
        if ctx.triggered_id == "flip-fixed":
            if not flip_clicks:
                raise PreventUpdate
            return flipped_fixed_source_info(source_info, slice_index)
        if ctx.triggered_id == "fixed-upload" and contents:
            return uploaded_fixed_source_info(contents, filename)
        raise PreventUpdate

    @app.callback(
        Output("fixed-slice-slider", "max"),
        Output("fixed-slice-slider", "value"),
        Output("fixed-slice-slider", "marks"),
        Output("fixed-slice-slider", "disabled"),
        Output("fixed-source-status", "children"),
        Input("fixed-source-store", "data"),
    )
    def update_fixed_slider(
        source_info: SourceInfo,
    ) -> tuple[int, int, dict[int, str], bool, str]:
        """Update the slice slider for the active fixed source.

        Parameters:
            source_info: Metadata for the active fixed source.

        Returns:
            Slider maximum, value, marks, disabled flag, and status text.
        """
        depth = int(source_info["depth"])
        slice_index = int(source_info["slice_index"])
        shape = source_info["shape"]
        status = (
            f"Loaded {source_info['name']} with displayed shape "
            f"{shape[0]} x {shape[1]} and {depth} slice(s)."
        )
        return depth - 1, slice_index, slice_slider_marks(depth), depth <= 1, status

    @app.callback(
        Output("moving-image-store", "data"),
        Input("moving-upload", "contents"),
        Input("rotate-moving", "n_clicks"),
        Input("flip-moving", "n_clicks"),
        State("moving-upload", "filename"),
        State("moving-image-store", "data"),
        prevent_initial_call=True,
    )
    def update_moving_image(
        contents: str | None,
        rotate_clicks: int | None,
        flip_clicks: int | None,
        filename: str | None,
        moving_data_uri: str,
    ) -> str:
        """Update the moving image store after an upload.

        Parameters:
            contents: Uploaded image data URI.
            rotate_clicks: Number of moving-image rotation button clicks.
            flip_clicks: Number of moving-image horizontal flip button clicks.
            filename: Uploaded filename.
            moving_data_uri: Stored moving image data URI.

        Returns:
            Data URI to store for the moving image.
        """
        if ctx.triggered_id == "rotate-moving":
            if not rotate_clicks:
                raise PreventUpdate
            return image_to_data_uri(rotate_clockwise(data_uri_to_image(moving_data_uri)))
        if ctx.triggered_id == "flip-moving":
            if not flip_clicks:
                raise PreventUpdate
            return image_to_data_uri(flip_horizontal(data_uri_to_image(moving_data_uri)))
        if ctx.triggered_id == "moving-upload" and contents:
            return uploaded_moving_image_data_uri(contents, filename)
        raise PreventUpdate

    @app.callback(
        Output("fixed-points-store", "data"),
        Input("fixed-graph", "clickData"),
        Input("undo-fixed", "n_clicks"),
        Input("reset-points", "n_clicks"),
        Input("fixed-slice-slider", "value"),
        Input("rotate-fixed", "n_clicks"),
        Input("rotate-moving", "n_clicks"),
        Input("flip-fixed", "n_clicks"),
        Input("flip-moving", "n_clicks"),
        State("fixed-points-store", "data"),
        prevent_initial_call=True,
    )
    def update_fixed_points(
        click_data: dict[str, Any] | None,
        undo_clicks: int | None,
        reset_clicks: int | None,
        slice_index: int | None,
        fixed_rotate_clicks: int | None,
        moving_rotate_clicks: int | None,
        fixed_flip_clicks: int | None,
        moving_flip_clicks: int | None,
        points: list[Point] | None,
    ) -> list[Point]:
        """Update fixed-image landmarks from clicks and controls.

        Parameters:
            click_data: Fixed graph click payload.
            undo_clicks: Number of fixed undo button clicks.
            reset_clicks: Number of reset button clicks.
            slice_index: Active fixed-volume slice index.
            fixed_rotate_clicks: Number of fixed-image rotation button clicks.
            moving_rotate_clicks: Number of moving-image rotation button clicks.
            fixed_flip_clicks: Number of fixed-image horizontal flip button clicks.
            moving_flip_clicks: Number of moving-image horizontal flip button clicks.
            points: Current fixed landmark list.

        Returns:
            Updated fixed landmark list.
        """
        triggered_id = ctx.triggered_id
        if triggered_id in {
            "reset-points",
            "fixed-slice-slider",
            "rotate-fixed",
            "rotate-moving",
            "flip-fixed",
            "flip-moving",
        }:
            return []
        if triggered_id == "undo-fixed":
            return undo_last_point(points)
        if triggered_id == "fixed-graph":
            return append_clicked_point(click_data, points)
        raise PreventUpdate

    @app.callback(
        Output("moving-points-store", "data"),
        Input("moving-graph", "clickData"),
        Input("undo-moving", "n_clicks"),
        Input("reset-points", "n_clicks"),
        Input("fixed-slice-slider", "value"),
        Input("rotate-fixed", "n_clicks"),
        Input("rotate-moving", "n_clicks"),
        Input("flip-fixed", "n_clicks"),
        Input("flip-moving", "n_clicks"),
        State("moving-points-store", "data"),
        prevent_initial_call=True,
    )
    def update_moving_points(
        click_data: dict[str, Any] | None,
        undo_clicks: int | None,
        reset_clicks: int | None,
        slice_index: int | None,
        fixed_rotate_clicks: int | None,
        moving_rotate_clicks: int | None,
        fixed_flip_clicks: int | None,
        moving_flip_clicks: int | None,
        points: list[Point] | None,
    ) -> list[Point]:
        """Update moving-image landmarks from clicks and controls.

        Parameters:
            click_data: Moving graph click payload.
            undo_clicks: Number of moving undo button clicks.
            reset_clicks: Number of reset button clicks.
            slice_index: Active fixed-volume slice index.
            fixed_rotate_clicks: Number of fixed-image rotation button clicks.
            moving_rotate_clicks: Number of moving-image rotation button clicks.
            fixed_flip_clicks: Number of fixed-image horizontal flip button clicks.
            moving_flip_clicks: Number of moving-image horizontal flip button clicks.
            points: Current moving landmark list.

        Returns:
            Updated moving landmark list.
        """
        triggered_id = ctx.triggered_id
        if triggered_id in {
            "reset-points",
            "fixed-slice-slider",
            "rotate-fixed",
            "rotate-moving",
            "flip-fixed",
            "flip-moving",
        }:
            return []
        if triggered_id == "undo-moving":
            return undo_last_point(points)
        if triggered_id == "moving-graph":
            return append_clicked_point(click_data, points)
        raise PreventUpdate

    @app.callback(
        Output("fixed-graph", "figure"),
        Output("moving-graph", "figure"),
        Output("points-summary", "children"),
        Input("fixed-source-store", "data"),
        Input("fixed-slice-slider", "value"),
        Input("moving-image-store", "data"),
        Input("fixed-points-store", "data"),
        Input("moving-points-store", "data"),
    )
    def refresh_display(
        fixed_source_info: SourceInfo,
        slice_index: int | None,
        moving_data_uri: str,
        fixed_points: list[Point] | None,
        moving_points: list[Point] | None,
    ) -> tuple[go.Figure, go.Figure, list[Any]]:
        """Refresh image figures and the landmark-pair summary.

        Parameters:
            fixed_source_info: Metadata for the active fixed source.
            slice_index: Active fixed-volume slice index.
            moving_data_uri: Stored moving image data URI.
            fixed_points: Current fixed-image landmarks.
            moving_points: Current moving-image landmarks.

        Returns:
            Fixed figure, moving figure, and summary children.
        """
        fixed_image = fixed_slice_image(fixed_source_info, slice_index)
        moving_image = data_uri_to_image(moving_data_uri)
        fixed_list = list(fixed_points or [])
        moving_list = list(moving_points or [])
        fixed_title = f"Fixed {fixed_source_info['name']} slice {int(slice_index or 0)}"
        return (
            image_figure(fixed_image, fixed_list, fixed_title),
            image_figure(moving_image, moving_list, "Moving image: img2"),
            points_summary(fixed_list, moving_list),
        )

    @app.callback(
        Output("registered-graph", "figure"),
        Output("overlay-graph", "figure"),
        Output("registration-status", "children"),
        Output("registered-image-store", "data"),
        Input("run-registration", "n_clicks"),
        State("fixed-source-store", "data"),
        State("fixed-slice-slider", "value"),
        State("moving-image-store", "data"),
        State("fixed-points-store", "data"),
        State("moving-points-store", "data"),
        State("stabilize-edges", "value"),
        prevent_initial_call=True,
    )
    def run_registration(
        run_clicks: int | None,
        fixed_source_info: SourceInfo,
        slice_index: int | None,
        moving_data_uri: str,
        fixed_points: list[Point] | None,
        moving_points: list[Point] | None,
        stabilize_values: list[str] | None,
    ) -> tuple[go.Figure, go.Figure, str, str | None]:
        """Run non-rigid registration and update result figures.

        Parameters:
            run_clicks: Number of registration button clicks.
            fixed_source_info: Metadata for the active fixed source.
            slice_index: Active fixed-volume slice index.
            moving_data_uri: Stored moving image data URI.
            fixed_points: Current fixed-image landmarks.
            moving_points: Current moving-image landmarks.
            stabilize_values: Checklist values controlling edge stabilization.

        Returns:
            Registered-image figure, overlay figure, status message, and image data URI.
        """
        if not run_clicks:
            raise PreventUpdate

        fixed_list = list(fixed_points or [])
        moving_list = list(moving_points or [])
        if len(fixed_list) != len(moving_list):
            message = (
                "Registration needs the same number of fixed and moving landmarks. "
                f"Current counts are {len(fixed_list)} and {len(moving_list)}."
            )
            return (
                empty_figure("Registered img2 in img1 space", message),
                empty_figure("Fixed/registered overlay", message),
                message,
                None,
            )

        if len(fixed_list) < 3:
            message = "Select at least 3 matching landmark pairs before registration."
            return (
                empty_figure("Registered img2 in img1 space", message),
                empty_figure("Fixed/registered overlay", message),
                message,
                None,
            )

        fixed_image = fixed_slice_image(fixed_source_info, slice_index)
        moving_image = data_uri_to_image(moving_data_uri)
        stabilize_edges = "stabilize" in (stabilize_values or [])
        try:
            registered = warp_moving_to_fixed(
                moving_image=moving_image,
                fixed_shape=fixed_image.shape[:2],
                fixed_points=fixed_list,
                moving_points=moving_list,
                stabilize_edges=stabilize_edges,
            )
        except Exception as error:
            message = f"Registration failed: {error}"
            return (
                empty_figure("Registered img2 in img1 space", message),
                empty_figure("Fixed/registered overlay", message),
                message,
                None,
            )

        overlay = blend_images(fixed_image, registered)
        message = (
            f"Registration complete using {len(fixed_list)} landmark pair(s). "
            "The registered image has the same pixel space as the selected img1 slice."
        )
        return (
            image_figure(registered, [], "Registered img2 in img1 space"),
            image_figure(overlay, fixed_list, "Fixed/registered overlay"),
            message,
            image_to_data_uri(registered),
        )

    @app.callback(
        Output("save-status", "children"),
        Input("save-registered", "n_clicks"),
        State("registered-image-store", "data"),
        State("fixed-slice-slider", "value"),
        prevent_initial_call=True,
    )
    def save_registered_result(
        save_clicks: int | None,
        registered_data_uri: str | None,
        slice_index: int | None,
    ) -> str:
        """Save the latest registered 2D image to disk.

        Parameters:
            save_clicks: Number of save button clicks.
            registered_data_uri: Registered image data URI from the latest run.
            slice_index: Fixed-volume slice index used for the filename.

        Returns:
            Status message with the output path.
        """
        if not save_clicks:
            raise PreventUpdate
        if not registered_data_uri:
            return "Run registration before saving a result."

        registered_image = data_uri_to_image(registered_data_uri)
        output_path = save_registered_image(registered_image, slice_index)
        return f"Saved registered 2D image to {output_path}."


def create_app() -> Dash:
    """Create and configure the Dash application.

    Parameters:
        None.

    Returns:
        Configured Dash app instance.
    """
    app = Dash(__name__)
    app.title = "Non-Rigid Image Registration"
    app.layout = app_layout
