def point_from_click(click_data: dict[str, Any] | None) -> Point:
    """Extract an image coordinate from Dash graph click data.

    Parameters:
        click_data: Dash clickData payload from a Plotly graph.

    Returns:
        Point dictionary containing x and y coordinates.
    """
    if not click_data or "points" not in click_data or not click_data["points"]:
        raise PreventUpdate

    point = click_data["points"][0]
    return {"x": float(point["x"]), "y": float(point["y"])}


def append_clicked_point(
    click_data: dict[str, Any] | None,
    points: list[Point] | None,
) -> list[Point]:
    """Append a clicked landmark to an existing point list.

    Parameters:
        click_data: Dash clickData payload from a Plotly graph.
        points: Existing landmark points.

    Returns:
        Updated landmark point list.
    """
    updated_points = list(points or [])
    updated_points.append(point_from_click(click_data))
    return updated_points


def undo_last_point(points: list[Point] | None) -> list[Point]:
    """Remove the most recently added landmark.

    Parameters:
        points: Existing landmark points.

    Returns:
        Point list without the final point.
    """
    updated_points = list(points or [])
    if updated_points:
        updated_points.pop()
    return updated_points


def landmarks_to_array(points: list[Point]) -> np.ndarray:
    """Convert point dictionaries to an Nx2 coordinate array.

    Parameters:
        points: Landmark points with x and y keys.

    Returns:
        Floating point coordinate array ordered as x, y.
    """
    return np.asarray([[point["x"], point["y"]] for point in points], dtype=float)


def boundary_landmarks(
    fixed_shape: tuple[int, int],
    moving_shape: tuple[int, int],
) -> tuple[np.ndarray, np.ndarray]:
    """Create boundary landmarks to stabilize extrapolation near image edges.

    Parameters:
        fixed_shape: Fixed image shape as height and width.
        moving_shape: Moving image shape as height and width.

    Returns:
        Pair of coordinate arrays for fixed-space and moving-space corners.
    """
    fixed_height, fixed_width = fixed_shape
    moving_height, moving_width = moving_shape
    fixed_points = np.asarray(
        [
            [0.0, 0.0],
            [fixed_width - 1.0, 0.0],
            [0.0, fixed_height - 1.0],
            [fixed_width - 1.0, fixed_height - 1.0],
        ],
        dtype=float,
    )
    moving_points = np.asarray(
        [
            [0.0, 0.0],
            [moving_width - 1.0, 0.0],
            [0.0, moving_height - 1.0],
            [moving_width - 1.0, moving_height - 1.0],
        ],
        dtype=float,
    )
    return fixed_points, moving_points


def warp_moving_to_fixed(
    moving_image: np.ndarray,
    fixed_shape: tuple[int, int],
    fixed_points: list[Point],
    moving_points: list[Point],
    stabilize_edges: bool,
    smoothing: float = 0.0,
) -> np.ndarray:
    """Warp the moving image into fixed-image coordinates using landmarks.

    Parameters:
        moving_image: RGB image that should be registered into fixed space.
        fixed_shape: Desired output shape as height and width.
        fixed_points: Landmarks selected in the fixed image.
        moving_points: Corresponding landmarks selected in the moving image.
        stabilize_edges: Whether to add image-corner constraints.
        smoothing: Thin-plate spline smoothing factor.

    Returns:
        Registered moving image with the fixed image shape.
    """
    target_points = landmarks_to_array(fixed_points)
    source_points = landmarks_to_array(moving_points)

    if stabilize_edges:
        fixed_boundary, moving_boundary = boundary_landmarks(
            fixed_shape=fixed_shape,
            moving_shape=moving_image.shape[:2],
        )
        target_points = np.vstack([target_points, fixed_boundary])
        source_points = np.vstack([source_points, moving_boundary])

    mapper = RBFInterpolator(
        target_points,
        source_points,
        kernel="thin_plate_spline",
        smoothing=smoothing,
    )

    fixed_height, fixed_width = fixed_shape
    grid_y, grid_x = np.mgrid[0:fixed_height, 0:fixed_width]
    target_grid = np.column_stack([grid_x.ravel(), grid_y.ravel()])
    source_grid = mapper(target_grid)

    source_x = source_grid[:, 0].reshape(fixed_height, fixed_width)
    source_y = source_grid[:, 1].reshape(fixed_height, fixed_width)
    registered = np.zeros((fixed_height, fixed_width, 3), dtype=np.float32)

    for channel in range(3):
        registered[:, :, channel] = map_coordinates(
            moving_image[:, :, channel],
            [source_y, source_x],
            order=1,
            mode="constant",
            cval=0.0,
        )

    registered = np.nan_to_num(registered, nan=0.0, posinf=255.0, neginf=0.0)
    return np.clip(registered, 0, 255).astype(np.uint8)


def fixed_to_moving_coordinate_grid(
    moving_shape: tuple[int, int],
    fixed_shape: tuple[int, int],
    fixed_points: list[Point],
    moving_points: list[Point],
    stabilize_edges: bool,
    smoothing: float = 0.0,
) -> tuple[np.ndarray, np.ndarray]:
    """Build source-coordinate grids from fixed-space landmarks.

    Parameters:
        moving_shape: Moving image shape as height and width.
        fixed_shape: Desired output shape as height and width.
        fixed_points: Landmarks selected in the fixed image.
        moving_points: Corresponding landmarks selected in the moving image.
        stabilize_edges: Whether to add image-corner constraints.
        smoothing: Thin-plate spline smoothing factor.

    Returns:
        Source y and x coordinate grids for resampling the moving image.
    """
    target_points = landmarks_to_array(fixed_points)
    source_points = landmarks_to_array(moving_points)

    if stabilize_edges:
        fixed_boundary, moving_boundary = boundary_landmarks(
            fixed_shape=fixed_shape,
            moving_shape=moving_shape,
        )
        target_points = np.vstack([target_points, fixed_boundary])
        source_points = np.vstack([source_points, moving_boundary])

    mapper = RBFInterpolator(
        target_points,
        source_points,
        kernel="thin_plate_spline",
        smoothing=smoothing,
    )
    fixed_height, fixed_width = fixed_shape
    grid_y, grid_x = np.mgrid[0:fixed_height, 0:fixed_width]
    target_grid = np.column_stack([grid_x.ravel(), grid_y.ravel()])
    source_grid = mapper(target_grid)
    source_x = source_grid[:, 0].reshape(fixed_height, fixed_width)
    source_y = source_grid[:, 1].reshape(fixed_height, fixed_width)
    return source_y, source_x


def warp_scalar_moving_to_fixed(
    moving_image: np.ndarray,
    fixed_shape: tuple[int, int],
    fixed_points: list[Point],
    moving_points: list[Point],
    stabilize_edges: bool,
    smoothing: float = 0.0,
) -> np.ndarray:
    """Warp scalar PC3 values into MRI slice coordinates.

    Parameters:
        moving_image: Scalar PC3 image to register.
        fixed_shape: Desired output shape as height and width.
        fixed_points: Landmarks selected in the MRI slice.
        moving_points: Corresponding landmarks selected in PC3.
        stabilize_edges: Whether to add image-corner constraints.
        smoothing: Thin-plate spline smoothing factor.

    Returns:
        Registered scalar PC3 image in MRI slice coordinates.
    """
    source_y, source_x = fixed_to_moving_coordinate_grid(
        moving_shape=moving_image.shape[:2],
        fixed_shape=fixed_shape,
        fixed_points=fixed_points,
        moving_points=moving_points,
        stabilize_edges=stabilize_edges,
        smoothing=smoothing,
    )
    registered = map_coordinates(
        moving_image.astype(np.float32),
        [source_y, source_x],
        order=1,
        mode="constant",
        cval=np.nan,
    )
    return registered.astype(np.float32)


def blend_images(fixed_image: np.ndarray, registered_image: np.ndarray) -> np.ndarray:
    """Blend fixed and registered images for visual quality control.

    Parameters:
        fixed_image: Reference image.
        registered_image: Moving image after registration.

    Returns:
        RGB overlay image.
    """
    blended = 0.5 * fixed_image.astype(float) + 0.5 * registered_image.astype(float)
    return np.clip(blended, 0, 255).astype(np.uint8)


def save_registered_image(image: np.ndarray, slice_index: int | None) -> Path:
    """Save a registered 2D image to the output directory.

    Parameters:
        image: Registered RGB image in fixed-slice pixel space.
        slice_index: Fixed-volume slice index used during registration.

    Returns:
        Path to the saved PNG image.
    """
    OUTPUT_DIR.mkdir(exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
