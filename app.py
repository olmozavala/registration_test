def selected_slice_affine(affine: np.ndarray, slice_index: int | None) -> np.ndarray:
    """Create a 2D NIfTI affine located at the selected MRI slice.

    Parameters:
        affine: MRI voxel-to-world affine.
        slice_index: Selected MRI slice index.

    Returns:
        Affine whose origin is shifted to the selected slice location.
    """
    safe_slice = int(slice_index or 0)
    output_affine = np.asarray(affine, dtype=float).copy()
    slice_origin = affine @ np.asarray([0.0, 0.0, float(safe_slice), 1.0])
    output_affine[:3, 3] = slice_origin[:3]
    return output_affine


def save_registered_nifti_slice(
    scalar_image: np.ndarray,
    fixed_source_info: SourceInfo,
    slice_index: int | None,
) -> Path:
    """Save registered PC3 as a 2D NIfTI in MRI world coordinates.

    Parameters:
        scalar_image: Registered scalar PC3 image in MRI slice pixel space.
        fixed_source_info: Metadata for the active MRI source.
        slice_index: Selected MRI slice index.

    Returns:
        Path to the saved NIfTI file.
    """
    if "affine_key" not in fixed_source_info:
        raise ValueError("The active MRI source does not have NIfTI affine metadata.")

    OUTPUT_DIR.mkdir(exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    safe_slice = int(slice_index or 0)
    output_path = OUTPUT_DIR / f"registered_pc3_slice_{safe_slice:03d}_{timestamp}.nii.gz"
    affine = selected_slice_affine(
        cached_affine(str(fixed_source_info["affine_key"])),
        slice_index,
    )
    nifti_image = nib.Nifti1Image(scalar_image.astype(np.float32), affine)
    nifti_image.header.set_xyzt_units("mm")
    nib.save(nifti_image, str(output_path))
    return output_path


def points_summary(fixed_points: list[Point], moving_points: list[Point]) -> list[Any]:
    """Build a compact point-pair summary for the interface.

    Parameters:
        fixed_points: Landmarks selected in the fixed image.
        moving_points: Landmarks selected in the moving image.

    Returns:
        Dash HTML children describing point-pair status.
    """
