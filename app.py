def normalize_slice_to_rgb(slice_data: np.ndarray) -> np.ndarray:
    """Convert a scalar 2D image slice into displayable RGB data.

    Parameters:
        slice_data: Scalar 2D image data.

    Returns:
        RGB uint8 image suitable for Plotly display.
    """
    finite_values = slice_data[np.isfinite(slice_data)]
    if finite_values.size == 0:
        normalized = np.zeros(slice_data.shape, dtype=np.uint8)
    else:
        low, high = np.percentile(finite_values, [1.0, 99.0])
        if np.isclose(low, high):
            low = float(np.min(finite_values))
            high = float(np.max(finite_values))
        if np.isclose(low, high):
            normalized = np.zeros(slice_data.shape, dtype=np.uint8)
        else:
            clipped = np.clip(slice_data, low, high)
            scaled = 255.0 * (clipped - low) / (high - low)
            scaled = np.nan_to_num(scaled, nan=0.0, posinf=255.0, neginf=0.0)
            normalized = np.clip(scaled, 0, 255).astype(np.uint8)

    return np.repeat(normalized[:, :, np.newaxis], 3, axis=2)


def normalize_scalar_image(slice_data: np.ndarray) -> np.ndarray:
    """Normalize a scalar image to the 0-to-1 display range.

    Parameters:
        slice_data: Scalar 2D image data.

    Returns:
        Floating point normalized image with values between 0 and 1.
    """
    finite_values = slice_data[np.isfinite(slice_data)]
    if finite_values.size == 0:
        return np.zeros(slice_data.shape, dtype=float)

    low, high = np.percentile(finite_values, [1.0, 99.0])
    if np.isclose(low, high):
        low = float(np.min(finite_values))
        high = float(np.max(finite_values))
    if np.isclose(low, high):
        return np.zeros(slice_data.shape, dtype=float)

    clipped = np.clip(slice_data, low, high)
    scaled = (clipped - low) / (high - low)
    scaled = np.nan_to_num(scaled, nan=0.0, posinf=1.0, neginf=0.0)
    return np.clip(scaled, 0.0, 1.0)


def scalar_to_red_blue_rgb(slice_data: np.ndarray) -> np.ndarray:
    """Convert a scalar PC3 image to a Matplotlib-like bwr RGB palette.

    Parameters:
        slice_data: Scalar 2D PC3 image data.

    Returns:
        RGB uint8 image using blue-white-red colors.
    """
    normalized = normalize_scalar_image(slice_data)
    red = np.minimum(1.0, 2.0 * normalized)
    green = np.where(normalized < 0.5, 2.0 * normalized, 2.0 * (1.0 - normalized))
    blue = np.minimum(1.0, 2.0 * (1.0 - normalized))
    rgb_image = np.stack([red, green, blue], axis=2)
    rgb_uint8 = np.clip(rgb_image * 255.0, 0, 255).astype(np.uint8)
    rgb_uint8[~np.isfinite(slice_data)] = [255, 255, 255]
    return rgb_uint8


def fixed_slice_image(source_info: SourceInfo, slice_index: int | None) -> np.ndarray:
    """Return the currently selected fixed image slice as RGB data.

    Parameters:
        source_info: Metadata for the cached fixed source.
        slice_index: Requested slice index for NIfTI data.

    Returns:
        RGB image displayed on the fixed side.
    """
    image = cached_image(str(source_info["key"]))
    if source_info["kind"] == "nifti":
        safe_index = int(slice_index or source_info["slice_index"])
        safe_index = max(0, min(safe_index, int(source_info["depth"]) - 1))
        return normalize_slice_to_rgb(image[:, :, safe_index])

    return image.astype(np.uint8)


def make_fixed_source_info(
    image: np.ndarray,
    name: str,
    kind: str,
    affine: np.ndarray | None = None,
) -> SourceInfo:
    """Create cache metadata for a fixed image source.

    Parameters:
        image: Fixed image or fixed volume data.
        name: Human-readable source name.
        kind: Source kind, either nifti or image.
        affine: Optional NIfTI affine matrix.

    Returns:
        Metadata used by Dash callbacks.
    """
    if kind == "nifti":
        depth = int(image.shape[2])
        slice_index = depth // 2
        shape = [int(image.shape[0]), int(image.shape[1]), depth]
    else:
        depth = 1
        slice_index = 0
        shape = [int(image.shape[0]), int(image.shape[1]), 1]

    source_info: SourceInfo = {
        "key": cache_image(image),
        "name": name,
        "kind": kind,
        "shape": shape,
        "depth": depth,
        "slice_index": slice_index,
    }
    if affine is not None:
        source_info["affine_key"] = cache_affine(affine)
    return source_info


def initial_fixed_source_info() -> SourceInfo:
    """Load the preferred default fixed image source.

    Parameters:
        None.

    Returns:
        Metadata for the default fixed NIfTI volume or PNG fallback.
    """
    if DEFAULT_FIXED_NIFTI.exists():
        volume, affine = load_nifti_volume_and_affine(DEFAULT_FIXED_NIFTI)
        return make_fixed_source_info(volume, DEFAULT_FIXED_NIFTI.name, "nifti", affine)

    image = load_image(DEFAULT_FIXED_IMAGE)
